/**
 * Translate a key for the given language, with optional string interpolation.
 * t('pt', 'dash.inDays', 3) → 'em 3d'
 * t('en', 'dash.inDays', 3) → 'in 3d'
 */
export function t(lang, key, ...args) {
  const dict = lang === 'en' ? EN : PT
  let str = dict[key] ?? PT[key] ?? key
  args.forEach((arg, i) => { str = str.replace(`{${i}}`, String(arg)) })
  return str
}

